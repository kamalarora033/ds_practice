/**
 * This enum defines the transformation type to be performed on the parameter.
 * 
 * @author Ericsson
 * 
 */
public enum ParamTransformationType {

	/**
	 * If no transformation is to be done.
	 */
	NONE,
	/**
	 * If negative transformation is to be done.
	 */
	NEGATIVE;

}
